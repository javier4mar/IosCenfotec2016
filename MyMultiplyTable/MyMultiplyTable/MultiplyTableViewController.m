@implementation MultiplyTableViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    
    self.tableResult = [MyTable getMyMultiply:7];
    self.navigationItem.rightBarButtonItem = self.editButtonItem;
    
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return self.tableResult.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"MultiplyCell" forIndexPath:indexPath];
    
    MyTable* row = nil;
    
    if(self.tableResult){
        
        row = [self.tableResult objectAtIndex:indexPath.row];
        
        cell.textLabel.text = [NSString stringWithFormat:@"%@ %@ %@ %@",row.number ,@" X ",row.multiply, @" = "] ;
        cell.detailTextLabel.text =[NSString stringWithFormat:@"%@",row.result] ;
    }
    
    return cell;
}


