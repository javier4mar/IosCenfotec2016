- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"cellNumber" forIndexPath:indexPath];
    
    // Configure the cell...
    MyNumber* number = nil;

    if(self.numbers){
    
        number = [self.numbers objectAtIndex:indexPath.row];

        cell.textLabel.text = number.number;
        cell.detailTextLabel.text =[NSString stringWithFormat:@"%@",number.primo] ;
    }
    
    return cell;
}


